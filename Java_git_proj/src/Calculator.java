public class Calculator {
    public static int plus(int first_number, int second_number){
        return (first_number+second_number);
    }
    public static int minus(int first_number, int second_number){
        return (first_number-second_number);
    }
    public static int multiplicate(int first_number, int second_number){
        return (first_number*second_number);
    }
    public static int devide(int first_number, int second_number){
        return (first_number/second_number);
    }
    public static int mod(int first_number, int second_number){
        return (first_number%second_number);
    }
}
